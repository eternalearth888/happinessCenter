
<h2>About Dr. Elia</h2>
<p>
<img src="images/EliaGourgouris_256px.jpg" vspace="0" hspace="0" align="LEFT" alt="Dr. Elia Gourgouris"style="margin-right: 20px; margin-bottom: 20px;">
Dr. Elia Gourgouris is the president of The Happiness Center - an 
organization dedicated to creating personal success and happiness. 
Over the last 18 years, he has helped thousands of people achieve happiness 
and fulfillment as individuals, both in their careers, and in their relationships.
Dr. Gourgouris has a remarkable ability to create and establish trust; he is also gifted with great insight, and is able to assist individuals and organizations identify areas of growth.&nbsp; In essence, he helps them bring out the best in themselves.
</p>
<p>In addition, Dr. Gourgouris, a nationally known speaker, has for the past several years been sought after as a monthly guest speaker and trainer for various organizations and conferences.&nbsp; These groups have benefited from his wise counsel mixed with humor regarding communication skills, decision making, women&#8217;s issues, self-esteem, and relationships.&nbsp; He also does leadership training sessions.</p>
<p>Dr. Gourgouris is a successful executive coach, life coach, and consultant with over eighteen years of experience.&nbsp; He received his B.A. in Psychology from UCLA and his M.A. and Ph.D. in Clinical Psychology from the California Graduate Institute.&nbsp; He currently consults with various individual and business clients throughout the United States.</p>
